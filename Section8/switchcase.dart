void main(){
var age=18;
  switch(age){
    case 1: print("You are too young to buy cigarretes");
      			break;
    case 18: print("You can buy cigareetes but remember, cigarretes are injurious to health");
      				break;
    case 90: print("You are too old to buy cigarretes");
      				break;
    default: print("This is a default statement");
  }
}
